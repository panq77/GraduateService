package pq.empm.ex;

public class updateError extends BaseEx {
    public updateError() {
        super();
    }

    public updateError(String message) {
        super(message);
    }

    public updateError(String message, Throwable cause) {
        super(message, cause);
    }

    public updateError(Throwable cause) {
        super(cause);
    }

    protected updateError(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
