package pq.empm.ex;

public class NoVisit extends BaseEx {
    public NoVisit() {
        super();
    }

    public NoVisit(String message) {
        super(message);
    }

    public NoVisit(String message, Throwable cause) {
        super(message, cause);
    }

    public NoVisit(Throwable cause) {
        super(cause);
    }

    protected NoVisit(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
