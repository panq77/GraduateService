package pq.empm.ex;

public class NoNotify extends BaseEx {
    public NoNotify() {
        super();
    }

    public NoNotify(String message) {
        super(message);
    }

    public NoNotify(String message, Throwable cause) {
        super(message, cause);
    }

    public NoNotify(Throwable cause) {
        super(cause);
    }

    protected NoNotify(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}

